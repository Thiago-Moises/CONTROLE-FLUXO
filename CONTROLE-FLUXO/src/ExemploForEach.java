public class ExemploForEach {

    /**
     * @param args
     */
    public static void main(String[] args) {

        String alunos[] = { "FELIPE", "THIAGO", "JULIA", "THAMIRIS" };

        for (int x = 0; x < alunos.length; x++) {

            System.out.println("Nome do aluno é : " + x + " é " + alunos[x]);
        }

    }

}
